package com.accenture.demo.graphql.model;


import org.springframework.stereotype.Component;

@Component
public class Friend {

	private Integer id;
	private String name;


	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
